    public void initRoomChat(ModelUser user, String passCode) {
        this.user = user;
        this.passCode = passCode;

        String query = "SELECT * FROM `chat` WHERE `passcode` = ?";
        listChat.clear();

        PreparedStatement st;
        ResultSet rs;
        try {
            st = DBconnection.getConnection().prepareStatement(query);
            st.setString(1, passCode);
            rs = st.executeQuery();

            while (rs.next()) {
                modelChat = new ModelChat();
                modelChat.setId_chat(rs.getInt("id_chat"));
                modelChat.setMessage(rs.getString("message"));
                modelChat.setDate(rs.getDate("date"));
                modelChat.setPassCode(rs.getString("passcode"));
                listChat.add(modelChat);
            }

        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultListModel pilih = new DefaultListModel();
        for (int i = 0; i < listChat.size(); i++) {
            pilih.addElement(listChat.get(i).getMessage());
        }
        this.jlist_chat.setModel(pilih);
    }

