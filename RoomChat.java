    private void addChat(String message) {
        PreparedStatement ps;
        ResultSet rs;

        String registerUserQuery = "INSERT INTO `chat`(`message`, `date`, `passcode`)VALUES(?,?,?)";

        try {
            Date date = new Date(System.currentTimeMillis());
            ps = DBconnection.getConnection().prepareStatement(registerUserQuery);
            ps.setString(1, user.getFull_name() + ": " + message);
            ps.setDate(2, date);
            ps.setString(3, passCode);

            if (ps.executeUpdate() != 0) {
                initRoomChat(user, passCode);
                this.edt_chat.setText("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

