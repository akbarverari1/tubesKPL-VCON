    public void TrackMeeting(String passCode) {
        String Query = "SELECT * FROM `meetings` WHERE `passcode` = ?";

        PreparedStatement PreparedStat;
        ResultSet ResultSett;
        ModelMeetings model = new ModelMeetings();

        try {
            PreparedStat = DBconnection.getConnection().prepareStatement(Query);
            PreparedStat.setString(1, passCode);
            ResultSett = PreparedStat.executeQuery();
            if (ResultSett.next()) {
                model.setId_meet(ResultSett.getInt("id_meeting"));
                model.setHost(ResultSett.getString("nama_host"));
                model.setJudul(ResultSett.getString("judul_meeting"));
                model.setSchedule(ResultSett.getDate("jadwal"));
                model.setNim(ResultSett.getInt("nim"));
                model.setPasscode(ResultSett.getString("passcode"));
                CreateMeeting meeting = new CreateMeeting();
                meeting.setVisible(true);
                meeting.pack();
                meeting.setLocationRelativeTo(null);
                meeting.JoinMeeting(User, model);
                this.dispose();
            } else {
                System.out.println("Pass Code tidak ada!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("cek:" + model.getJudul() + " : " + model.getHost());
    }

