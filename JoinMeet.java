    public void InitUsername(String Username) {
        this.Username = Username;
        String Query = "SELECT * FROM `users` WHERE `username` = ?";

        PreparedStatement PreparedStat;
        ResultSet ResultSett;
        try {
            PreparedStat = DBconnection.getConnection().prepareStatement(Query);
            PreparedStat.setString(1, Username);
            ResultSett = PreparedStat.executeQuery();
            User = new ModelUser();
            if (ResultSett.next()) {

                User.setNim(ResultSett.getInt("nim"));
                User.setFull_name(ResultSett.getString("full_name"));
                User.setGender(ResultSett.getString("gender"));
                User.setPhone(ResultSett.getString("phone"));
                User.setUsername(ResultSett.getString("username"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("cek:" + User.getUsername() + " : " + User.getNim());
    }

